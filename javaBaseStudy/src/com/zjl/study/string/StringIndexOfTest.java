public class StringIndexOfTest {
	public static void main(String[] args) {
		System.out.println("asdfasdf".indexOf(""));
		System.out.println("asdfasdf".indexOf("asd"));
		System.out.println("asdfasdf".indexOf("lmn"));
		System.out.println("".indexOf("lmn"));
		System.out.println("".indexOf(""));
	}
}
